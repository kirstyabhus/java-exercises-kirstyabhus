package com.cbfacademy;

public class ArithmeticExercises {
    Float AddFloat(float x, float y) {
        return x + y;
    }

    Float SubtractFloat(float x, float y) {
        return x - y;
    }

    Float MultiplyFloat(float x, float y) {
        return x * y;
    }

    Float DivideFloat(float x, float y) {
        return x / y;
    }
}
